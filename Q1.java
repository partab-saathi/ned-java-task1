// Q1:
// Create a program to perform addition, subtraction, multiplication, and division on two numbers entered by the user.
public class Q1 {
    public static void main(String[] args) {
        int x;
        int y;
        x=10;
        y=2;
       System.out.println("x = "+ x);
       System.out.println("y = "+ y);
       System.out.println("x+y=" + (x+y));
       System.out.println("x-y=" + (x-y));
       System.out.println("x*y=" + (x*y));
       System.out.println("x/y=" + (x/y));
    }
}

